# To slice a string you use string notation
my_string = 'I am a string!'
print(my_string[2:4])

# The string notation is EXCLUSIVE so 2 is not read (4 is)
# It also is based on index numbers (0 1 2)
my_string = '0123456789'
print(my_string[2:-4])
